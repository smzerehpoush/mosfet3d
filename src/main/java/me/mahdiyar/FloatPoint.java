package me.mahdiyar;


public class FloatPoint {

    private double x;
    private double y;

    public FloatPoint() {
        x = 0.0D;
        y = 0.0D;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
